public class Multiplier {
	
	
    /**
     * <h3>Mana klase</h3>
     * @param x aaa
     * @param y bbb
     * @return
     */
    public int calculate(int x,int y) { 
        return x * y;
    }
    
    public String toString(int x,int y) { 
        return "x * y = "+x+"*"+y+calculate(x,y);
    }
    
    

}
